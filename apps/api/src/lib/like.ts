/**
 * Экранирует спецсимволы LIKE/ILIKE (`%`, `_`, `\`) в пользовательской строке,
 * чтобы они трактовались как литералы, а не как wildcard'ы.
 *
 * Это НЕ про инъекции — значение и так передаётся bind-параметром (SQL-инъекции
 * нет). Это про корректность поиска: без экранирования запрос «100%» матчит
 * «100<что угодно>», а «имя_1» — «имя<любой символ>1». Postgres по умолчанию
 * использует `\` как escape-символ в LIKE/ILIKE, поэтому отдельная ESCAPE-клауза
 * не нужна — достаточно заэкранировать сами символы в подставляемом значении.
 *
 * Применять к пользовательскому `q` ДО оборачивания в wildcard'ы:
 *   ilike(col, `%${escapeLike(q)}%`)
 */
export function escapeLike(input: string): string {
  return input.replace(/[\\%_]/g, '\\$&');
}
