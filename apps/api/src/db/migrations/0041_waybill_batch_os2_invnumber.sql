# Признаки форм

**Форма 2116 (Транспортная накладная):**
- Заголовок «Транспортная накладная (форма)» или «Транспортная накладная».
- Указание Постановления Правительства РФ № 2116 от 30 ноября 2021 г. (Приложение № 4).
- Пронумерованные разделы: 1. Грузоотправитель, 2. Грузополучатель, 3. Груз, 4. Сопроводительные документы, 5. Указания, 6. Перевозчик, 7. Транспортное средство, и т.д.
- Печатный шрифт (НЕ рукописный).

**Форма ОС-2 (Внутреннее перемещение основных средств):**
- Заголовок «НАКЛАДНАЯ на внутреннее перемещение объектов основных средств».
- Указание унифицированной формы № ОС-2, утв. постановлением Госкомстата России от 21.01.2003 № 7.
- Код по ОКУД 0306032.
- Шапка: «Сдатчик» и «Получатель» (внутренние подразделения и МОЛы, а НЕ внешние контрагенты).
- Таблица «Объект основных средств» с колонками: Номер по порядку | Наименование | Дата приобретения | Инвентарный номер | Количество шт. | Стоимость единицы | Стоимость всего.

# Что извлекать на каждый документ

## Общие поля (есть у обеих форм)

- `form` — `"tn_2116"` или `"os2"`.
- `docNumber` — номер документа из шапки (например «СП00-006047», «Д0000002518»).
- `docDate` — дата документа в формате YYYY-MM-DD.
- `confidence` — число 0..1, оценка качества распознавания именно этого документа.

## Для ТН-2116 (form = "tn_2116")

- `shipper` — Грузоотправитель из раздела 1:
  - `inn` — ИНН (10 или 12 цифр).
  - `name` — полное название организации.
- `consignee` — Грузополучатель из раздела 2:
  - `inn` — ИНН.
  - `name` — полное название.
- `items` — позиции из раздела 3 «Груз»:
  - `nameRaw` — наименование как есть.
  - `qty` — количество (число).
  - `unit` — единица измерения («шт», «м³», «упак», «рул», «поддон» и т.п.).
  - `invNumber` — null (у ТН нет инвентарных номеров).
  - `price` — null (в шапке ТН цены обычно нет).
  - `sum` — null.
- Для ТН поля `sender`, `recipient`, `totalSum` верни null.

## Для ОС-2 (form = "os2")

- `sender` — Сдатчик (внутренний отправитель):
  - `name` — текст из строки «Сдатчик» (например «Медников Р.С. Основной склад IT»).
  - `department` — текст из строки «(наименование структурного подразделения)» если виден.
- `recipient` — Получатель (внутренний получатель):
  - `name` — текст из строки «Получатель» (например «Барулин И.А. Лётная ул., ЖК «ALIA»…»).
  - `department` — текст из строки «(наименование структурного подразделения)» если виден.
- `items` — позиции из таблицы «Объект основных средств»:
  - `nameRaw` — колонка 2 «Наименование» (например «Монитор 27" MSI Pro MP272 сер.№PD6T085B00283 (119866)»).
  - `invNumber` — колонка 4 «Инвентарный номер» (например «119866»).
  - `qty` — колонка 5 «Количество, шт.» (обычно 1).
  - `unit` — всегда «шт» для ОС-2.
  - `price` — колонка 6 «Стоимость единицы» (например 10500.00).
  - `sum` — колонка 7 «Стоимость всего» (например 10500.00).
- `totalSum` — «Итого по документу» под таблицей (например 21000.00).
- Для ОС-2 поля `shipper`, `consignee` верни null.

# Confidence (на каждый документ)

- 0.9–1.0 — документ найден, все поля разборчивы.
- 0.7–0.9 — мелкие неточности (одно поле размыто).
- 0.4–0.7 — часть данных размыта/обрезана, но форма точно опознана.
- 0.0–0.4 — форма опознана с трудом.

