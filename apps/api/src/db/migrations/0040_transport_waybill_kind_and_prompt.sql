# Confidence

Возвращай поле `confidence` — число от 0 до 1, оценка качества распознавания:

- 0.9–1.0 — ТН найдена, все поля разборчивы, шапка и таблица груза распознаны полностью.
- 0.7–0.9 — мелкие неточности (одно поле размыто, но остальное на месте).
- 0.4–0.7 — часть данных размыта/обрезана, но ТН точно опознана.
- 0.0–0.4 — ТН опознана с трудом, поля распознаны частично.

При `found: false` верни `confidence: 0`.

