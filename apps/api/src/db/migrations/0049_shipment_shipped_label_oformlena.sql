-- Лейбл статуса shipment.shipped был «Отгружено» (миграция 0007). Семантически
-- это полный аналог delivery.filled «Оформлена» — оба означают «1 Этап готов,
-- ждём подтверждения МОЛ (confirmed_mol)». Для визуального паритета Отгрузки
-- с Приёмкой в портале (Операции → Принятые) переименовываем в «Оформлена».
-- Данные не теряются — UPDATE затрагивает только колонку label в таблице
-- statuses. Все связи statuses_id у уже существующих shipments указывают на ту
-- же строку, просто отображаемый текст становится «Оформлена».
UPDATE "statuses"
SET "label" = 'Оформлена'
WHERE "entity_type" = 'shipment' AND "code" = 'shipped';
