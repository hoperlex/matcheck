# Проверка корректности qty и price

После извлечения проверь: qty × price ≈ sum (с точностью до копеек или
нескольких рублей за счёт округления поставщиком). Если равенство грубо
нарушено (отличается в десятки/сотни раз) — почти наверняка qty и price
перепутаны местами, либо одно из них является кодом товара или ОКЕИ. В
этом случае переоцени распределение по колонкам.

ВАЖНО: если в источнике видна и колонка 4 (цена без налога), и колонка 9
(стоимость с налогом), всё равно возвращай price = sum / qty (с НДС), а
не значение из колонки 4. В портале price используется для отображения
«Цена», и она должна быть согласована с «Сумма» (sum) на одном и том же
налоговом уровне — с НДС.

