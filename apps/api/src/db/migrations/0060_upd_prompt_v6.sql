# Поле confidence (ОБЯЗАТЕЛЬНОЕ)

В каждом ответе возвращай поле `confidence` — число от 0 до 1, оценка
качества распознавания. Шкала:

  0.9–1.0 — все строки распознаны, qty × price ≈ sum для каждой строки
            (с точностью до рубля), шапочные реквизиты на месте
            (docNumber, docDate, totalSum, supplier.inn, recipient.inn).
  0.7–0.9 — мелкие неточности: qty × price расходится с sum в пределах
            рубля (округление поставщиком), не хватает 1–2 второстепенных
            полей шапки.
  0.4–0.7 — есть сомнительные строки или подозрение на перепутанные
            колонки (qty × price отличается от sum в десятки раз).
  0.0–0.4 — серьёзные проблемы: часть таблицы не распознана, нет шапки,
            не нашёл supplier/recipient.

Это поле ОБЯЗАТЕЛЬНОЕ. Если confidence отсутствует — ответ невалиден
(валидация отклонит документ как parse_failed).

# Объём и масса единицы товара (для прогноза заполнения транспорта)

- volumeM3 — оценочный габаритный объём ОДНОЙ единицы товара в м³.
