-- Сид/обновление справочника объектов строительства из JSON-файла
-- заказчика. До этой миграции в sites было только локальные записи
-- (NA «Без объекта», ЗИЛ18, КНГ, СТРС); после неё — все ЖК-объекты
-- из ФОТ-источника заказчика, итого 34 ЖК + локальные.
--
-- Стратегия НЕ-разрушительная: никаких DELETE/TRUNCATE. UUID-ы
-- существующих записей сохраняются, поэтому все FK-связи (deliveries.
-- site_id, shipments.site_id, users.site_id для inspector_kpp,
-- source_documents.site_id, S3-префикс фото `<siteId>/`) остаются
-- целыми.

-- 1. Расширяем varchar(5) → varchar(16). В JSON-источнике есть коды
--    длиной 6 (ПРИМ22, ПРИМ13, ПРИМ14) и с точкой (МЕ1.0, МЕ2.0).
ALTER TABLE sites ALTER COLUMN code TYPE VARCHAR(16);

-- 2. Колонка для внешнего стабильного id (analog employee_id у МОЛ).
--    Локально созданные объекты имеют NULL. Через UI ФОТ-записи
--    read-only — см. routes/sites.ts (409 fot_readonly).
ALTER TABLE sites ADD COLUMN fot_site_id BIGINT;

CREATE UNIQUE INDEX sites_fot_site_id_uidx
  ON sites (fot_site_id)
  WHERE fot_site_id IS NOT NULL;

-- 3. Перемап существующих локальных кодов на ФОТ-коды, чтобы UPSERT
--    ниже захватил их по месту (UUID не меняется → FK живы).
--    ЗИЛ18 совпадает с ФОТ-кодом — переименовывать не нужно.
UPDATE sites SET code = 'КИНГ' WHERE code = 'КНГ';
UPDATE sites SET code = 'СТОР' WHERE code = 'СТРС';

