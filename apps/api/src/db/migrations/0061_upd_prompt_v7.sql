# Структура стандартной формы УПД (Постановление Правительства РФ № 1137)

Таблица товаров содержит колонки в строгом порядке:
  1.  № п/п — порядковый номер строки в документе (1, 2, 3, ...).
  1а. Код вида товара (опционально).
  2.  Код по ОКЕИ — числовой код единицы измерения (см. ниже).
  2а. Условное обозначение единицы измерения — текст («шт», «м³» и т.п.).
  3.  Количество (qty).
  4.  Цена (тариф) за единицу измерения БЕЗ налога (price).
  5.  Стоимость товаров (работ, услуг), имущественных прав без налога — всего.
  6.  В том числе сумма акциза (опционально).
  7.  Налоговая ставка (vatRate) — обычно 20, 10 или 0; иногда «Без НДС».
  8.  Сумма налога, предъявляемая покупателю (vatSum) — НДС по строке в рублях.
  9.  Стоимость товаров (работ, услуг), имущественных прав С НАЛОГОМ — всего.
  10. Страна происхождения — цифровой код.
  10а. Краткое наименование страны происхождения.
  11. Регистрационный номер декларации на товары (опционально).

# Извлекаемые поля для каждой строки таблицы

- nameRaw: наименование строкой как есть (колонка 3).
- qty:    количество (колонка 3 «Количество»). КРИТИЧНО: это НЕ код товара и НЕ код ОКЕИ.
- unit:   единица измерения (название, колонка 2а).
- price:  цена единицы БЕЗ НАЛОГА — берём значение строго из графы 4
          формы УПД («Цена/тариф за единицу измерения»). НЕ вычисляй
          как sum / qty: это значение мы хотим видеть таким же, как в
          печатной форме первоисточника.
- sum:    стоимость С НАЛОГОМ — всего по строке (графа 9). ОБЯЗАТЕЛЬНО
          берём «Стоимость с налогом — всего», а НЕ «Стоимость без
          налога». Если в УПД присутствуют обе колонки — sum = с
          налогом. Если налога нет вовсе (vatRate = 0 или «Без НДС») —
          sum равна графе 5 (они в этом случае совпадают).
- vatRate: налоговая ставка по строке в процентах (колонка 7). Целое или
          с одной цифрой после запятой: 20, 10, 0. Если в строке стоит
          «Без НДС» или прочерк — vatRate = null.
- vatSum: сумма налога по строке в рублях (колонка 8). Это ОТДЕЛЬНАЯ
          колонка УПД, НЕ равна `sum`. Если строка «Без НДС» — vatSum = null
          или 0. Соотношение для проверки: vatSum ≈ sum × vatRate / (100 + vatRate),
          а также sum = (графа 5) + vatSum.

