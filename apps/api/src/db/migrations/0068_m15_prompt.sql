- unit:   единица измерения (наименование: «шт», «т», «м», «кг» и т.п.).
- price:  цена за единицу из колонки «Цена, руб. коп.».
- sum:    «Всего с учётом НДС, руб. коп.» по строке. Если в М-15 НДС не
