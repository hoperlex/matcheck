-- Этап приёмки, к которому относится фото: 'before' — снято на 1-м этапе
-- (приёмка/осмотр на КПП), 'after' — снято на 2-м этапе (после выгрузки и
-- подтверждения МОЛ). Источник истины — мобильный клиент: он знает, на каком
-- экране пользователь нажал «Сделать фото», и отправляет stage в presign.
--
-- Default 'before' нужен, чтобы:
-- 1) Backfill для уже существующих фото был корректным — все они логически
--    относятся к 1-му этапу (фичи «после» до этой миграции не было).
-- 2) Старые версии мобильного и веб-клиента, не присылающие stage, продолжали
--    работать — фото попадёт в «До».
--
-- Для shipment_photos этап не вводим — у отгрузки нет двух этапов.
CREATE TYPE "delivery_photo_stage" AS ENUM ('before', 'after');

ALTER TABLE "delivery_photos"
  ADD COLUMN "stage" "delivery_photo_stage" NOT NULL DEFAULT 'before';
