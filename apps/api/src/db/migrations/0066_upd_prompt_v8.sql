  ни маркировки, ни единицы измерения с понятной геометрией). При наличии
  единицы вида м/м²/м³/т/пог.м оценка ОБЯЗАТЕЛЬНА — пусть и с
  volumeConfidence='low'.

- massKg — оценочная масса ОДНОЙ единицы в кг с упаковкой.
