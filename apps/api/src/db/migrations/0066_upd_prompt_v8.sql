# Проверка корректности qty и price

price (без НДС) и sum (с НДС) — на разных налоговых базах, поэтому
qty × price ≠ sum в общем случае. Правильное соотношение:
  qty × price ≈ sum × 100 / (100 + vatRate),  то есть «база без НДС».
Если qty × price отличается от sum × 100 / (100 + vatRate) в десятки
раз — почти наверняка qty распознано как код товара или код ОКЕИ; в
этом случае переоцени распределение по колонкам.

# Если в пакете несколько страниц

Пакет страниц/изображений может содержать НЕ ТОЛЬКО УПД, а также:
  - транспортные накладные (ТН-2, форма Минтранса);
  - спецификации к договору;
  - паспорта качества, сертификаты;
  - акты сверки;
  - рукописные подписи и печати.
Извлекай позиции ТОЛЬКО из страниц УПД (страница с заголовком
«Счёт-фактура № …» и табличной частью граф 1, 1а, 2, 2а, 3, 4, 5, 6,
7, 8, 9, 10, 10а, 11 по форме ПП №1137). Все остальные документы в
пакете ИГНОРИРУЙ: их содержимое не должно попадать в items, totalSum,
supplier, recipient. Шапочные реквизиты тоже берём только со страницы
УПД.

Если в пакете несколько страниц самой УПД (длинная табличная часть) —
объединяй позиции по всем УПД-страницам в общий items[].

