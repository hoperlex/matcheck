# Поле confidence (ОБЯЗАТЕЛЬНОЕ)

В каждом ответе возвращай поле `confidence` — число от 0 до 1, оценка
качества распознавания. Шкала:

  0.9–1.0 — все строки распознаны, qty × price ≈ sum × 100 / (100 + vatRate)
            (с точностью до рубля), шапочные реквизиты на месте
            (docNumber, docDate, totalSum, supplier.inn, recipient.inn).
  0.7–0.9 — мелкие неточности: расхождение базы без НДС в пределах рубля
            (округление поставщиком), не хватает 1–2 второстепенных
            полей шапки.
  0.4–0.7 — есть сомнительные строки или подозрение на перепутанные
            колонки (qty × price отличается в десятки раз от базы без НДС).
  0.0–0.4 — серьёзные проблемы: часть таблицы не распознана, нет шапки,
            не нашёл supplier/recipient.

Это поле ОБЯЗАТЕЛЬНОЕ. Если confidence отсутствует — ответ невалиден
(валидация отклонит документ как parse_failed).

# Объём и масса единицы товара (для прогноза заполнения транспорта)

- volumeM3 — оценочный габаритный объём ОДНОЙ единицы товара в м³.
  Источники для оценки (в порядке приоритета):
    1) Явные размеры в наименовании: HxW, L=, Ф, R, толщина, диаметр.
    2) Стандартизованные маркировки ГОСТ:
       ПК 60.15.8 = 6×1.5×0.22 м;
       ФБС 24.6.6 = 2.4×0.6×0.6 м;
       кирпич КР-р-по 250×120×65 мм;
       газобетон 625×250×200 мм;
       ГКЛ 2500×1200×12.5 мм;
       арматура Ø10 А500 = 7.85 кг/м, объёмом ≈ 0.0000785 м³/м.
    3) Типичные упаковки: мешок цемента 25 кг ≈ 0.02 м³;
       паллета 1.2×0.8×1.5 м; рулон минваты 6×1.2×0.1 м;
       бухта кабеля Ø=0.5 м, толщина 0.1 м.
    4) Если единица — м, м², м³, т, погонный метр — рассчитай объём:
       - м³: одна единица = 1 м³ (volumeM3 = 1).
       - м²: одна единица = 1 м² × стандартная толщина (плитка 8 мм →
         0.008 м³; ГКЛ 12.5 мм → 0.0125 м³; ОСП 9 мм → 0.009 м³).
       - м (пог): зависит от профиля; для арматуры/трубы — оцени по диаметру.
       - т: при известной плотности материала — volumeM3 = 1000 / плотность.
