-- Заполняется инспектором с мобилы в dropdown'е на форме «Новая отгрузка»
-- (empty-draft, без УПД). 4 значения от мобилы: «Вывоз материала»,
-- «Перемещение на объект», «Вывоз мусора», «Другое». На веб-портале
-- отображается чипом «Тип отгрузки: …» в шапке карточки рядом с
-- «Откуда / Получатель / Госномер / Водитель».
--
-- NULL — для отгрузок с привязанной УПД (тип определяется самим
-- документом) и для legacy-записей, созданных до этой миграции.
--
-- Тип text (не enum), чтобы при необходимости расширить список значений
-- не требовалась миграция БД. Валидация — только на клиенте.

