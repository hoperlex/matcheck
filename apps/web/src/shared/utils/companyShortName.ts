/**
 * Сокращение названия юрлица до читабельного префикса.
 *
 * Пример: «ОБЩЕСТВО С ОГРАНИЧЕННОЙ ОТВЕТСТВЕННОСТЬЮ "АРСТ"» → «ООО "АРСТ"».
 *
 * Когда нужно: в столбцах таблиц («Поставщик», «Подрядчик»), где ширина
 * колонки фиксирована и полное название уезжает в ellipsis. Короткий
 * префикс даёт пользователю сразу увидеть «АРСТ», а не «Общество с ог…».
 *
 * Безопасно: если префикс не распознан, возвращает исходную строку как
 * есть. Не модифицирует часть после ОПФ — кавычки, регистр и слова
 * сохраняются.
 */
const PREFIX_MAP: Array<[RegExp, string]> = [
  // ОПФ от более длинных к коротким — порядок важен для регулярок,
  // которые могут пересекаться (например, «АКЦИОНЕРНОЕ ОБЩЕСТВО» —
  // суффикс «ЗАКРЫТОГО АКЦИОНЕРНОГО ОБЩЕСТВА»). Якорь `^` отсекает
  // совпадение в середине, но для надёжности упорядочиваем.
  [/^публичное\s+акционерное\s+общество\s+/iu, 'ПАО '],
  [/^непубличное\s+акционерное\s+общество\s+/iu, 'НАО '],
  [/^закрытое\s+акционерное\s+общество\s+/iu, 'ЗАО '],
  [/^открытое\s+акционерное\s+общество\s+/iu, 'ОАО '],
  [/^общество\s+с\s+ограниченной\s+ответственностью\s+/iu, 'ООО '],
  [/^акционерное\s+общество\s+/iu, 'АО '],
  [/^индивидуальный\s+предприниматель\s+/iu, 'ИП '],
];

export function shortenCounterpartyName(name: string | null | undefined): string {
  if (!name) return '—';
  const trimmed = name.trim();
  if (!trimmed) return '—';
  for (const [re, replacement] of PREFIX_MAP) {
    if (re.test(trimmed)) {
      return trimmed.replace(re, replacement);
    }
  }
  return trimmed;
}
